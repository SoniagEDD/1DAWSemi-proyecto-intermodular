-- Primero borras (bien hecho el orden de borrado)

drop table if exists reserva cascade; 
drop table if exists pasaporte cascade; 
drop table if exists guia cascade;
drop table if exists destino cascade;
drop table if exists usuario cascade;

drop type if exists especialidad_guia cascade;

create type especialidad_guia as enum ('Geografia','Historia','Arquitectura','Comida');

-- 1. TABLAS SIN DEPENDENCIAS
create table usuario (
    id integer generated by default as identity,
    nombre varchar(255),
    apellidos varchar(255),
    email varchar(255),
    telefono varchar(255),
    fecha_nacimiento date,
    primary key (id)
);

create table destino (
    id integer generated by default as identity,
    ciudad varchar(255),
    pais varchar(255),
    precio numeric(10,2),
    requiere_pasaporte boolean,
    primary key (id)
);

-- 2. TABLAS QUE DEPENDEN DE OTRAS
create table guia (
    id integer generated by default as identity,
    nombre varchar(255),
    apellidos varchar(255),
    especialidad especialidad_guia,
    primary key (id),
    id_destino integer unique,
    constraint FK_guia_destino foreign key (id_destino) references destino(id)
);

create table pasaporte (
    fecha_caducidad date,
    id integer generated by default as identity,
    id_usuario integer unique,
    numero varchar(255),
    pais_expedicion varchar(255),
    primary key (id),
    constraint fk_pasaporte_usuario foreign key (id_usuario) references usuario(id)
);

create table reserva (
	id SERIAL,
    id_usuario integer,
    id_destino integer,
    fecha_inicio date,
    fecha_fin date,
    primary key (id_usuario, id_destino),
    constraint FK_reserva_usuario foreign key (id_usuario) references usuario(id)
        on delete restrict
        on update cascade,
    constraint FK_reserva_destino foreign key (id_destino) references destino(id)
        on delete restrict
        on update cascade
);